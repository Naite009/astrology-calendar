// Helper function to get gate by number
export const getGateByNumber = (num: number): HDGate | undefined => {
  return HUMAN_DESIGN_GATES.find(g => g.number === num);
};

// Get all gates for a specific center
export const getGatesByCenter = (center: HDCenterName): HDGate[] => {
  return HUMAN_DESIGN_GATES.filter(g => g.center === center);
};
