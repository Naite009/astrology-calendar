const buildDirective = (
  sectionList: Array<{ type: string; title: string }>,
  callLabel: "NATAL-ONLY" | "SR-ONLY" | "OVERLAY",
  extraInstruction = "",
): string => {
  const sectionLines = sectionList
    .map((s, i) => `  ${i + 1}. type="${s.type}", title="${s.title}"`)
    .join("\n");

  return `=========================================================
PER-CALL DIRECTIVE — ${callLabel} CALL
=========================================================

FOR THIS CALL ONLY you are writing these sections:
${sectionLines}

You MUST emit valid JSON with this exact top-level shape:
{
  "sections": [ /* exactly the ${sectionList.length} section(s) listed above, in that order */ ]
}

The "sections" array must contain ONLY those ${sectionList.length} section(s) — no extras, no substitutions, no merging. Do not include modality_element, do not include polarity counts, do not include element/modality tallies anywhere — those are calculated separately in code and are not your responsibility on this call.

The chart data in the user message is the ONLY source of truth for planet positions in this call. Do not infer, recall, or reference any planet position not present in the chart data provided below. If a placement you would normally cite is not in the chart data for this call, write the section without that placement — do not invent a position from memory or from a different chart.

${extraInstruction}

Output: a single JSON object, no prose before or after, no markdown code fences.`;
};

