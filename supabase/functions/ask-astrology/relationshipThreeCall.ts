const buildSharedSystemBlocks = (
  masterSystemPrompt: string,
  chartScopedRulesShared: string,
  perCallDirective: string,
  effectiveCurrentDate: string,
): Array<Record<string, any>> => {
  // Block 1: master SYSTEM_PROMPT — identical across A/B/C and across users.
  //          Cacheable. Holds every existing rule (clarity translation,
  //          jargon ban, gate aliases, voice, etc.).
  // Block 2: chart-scoped shared rules (Lilith/Juno gates, SR-year mute) —
  //          identical across A/B/C for THIS chart. Cacheable.
  // Block 3: per-call directive + current-date. Differs per call; uncached.
  const blocks: Array<Record<string, any>> = [
    { type: "text", text: masterSystemPrompt, cache_control: { type: "ephemeral" } },
  ];
  if (chartScopedRulesShared) {
    blocks.push({ type: "text", text: chartScopedRulesShared, cache_control: { type: "ephemeral" } });
  }
  blocks.push({
    type: "text",
    text: `${perCallDirective}\n\n--- CURRENT LOCAL DATE ---\n${effectiveCurrentDate}`,
  });
  return blocks;
};

