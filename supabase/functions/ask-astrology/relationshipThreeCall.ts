// ============================================================================
// 3-CALL RELATIONSHIP READING ORCHESTRATOR
// ============================================================================
// Purpose: Stop SR/natal data bleed in relationship readings by physically
// separating the chart data the model can see in each generation.
//
// Architecture (per user spec):
//   Call A (parallel) — natal chart data ONLY. Author the natal-anchored
//                       sections. Model literally cannot reference SR
//                       positions because they aren't in its user message.
//   Call B (parallel) — SR chart data ONLY. Author the SR/timing sections.
//                       Model literally cannot reference natal positions.
//   Call C (sequential, after A+B) — both charts in user message, explicitly
//                       labeled NATAL CHART vs SOLAR RETURN CHART. Author
//                       overlay sections only.
//
// All three calls share ONE master system prompt (so existing hygiene/gate
// rules stay intact) plus a per-call directive that lists the exact sections
// the call must produce and forbids referencing planet data not in its user
// message.
//
// Persistence:
//   - Each successful call's output is written to ask_jobs.call_*_output
//     immediately so a retry can resume from the failed call only.
//   - call_status tracks {a,b,c}: "done" | "failed" | undefined.
//
// Resilience:
//   - 150 second per-call timeout via AbortSignal.timeout.
//     (Sonnet reliably needs 60–120s per call for one third of a full
//     relationship reading; 45s was too tight and timed out 100% of jobs.)
//   - Up to 2 attempts per call with exponential backoff (1.5s, 4.5s).
//   - On final failure, the whole job is marked failed but successful
//     call outputs remain in their columns for resume.
//
// Polarity / element tallies are NOT generated by the model. They are
// computed deterministically from natal positions in code by the caller
// and merged into the final JSON.
// ============================================================================

const PER_CALL_TIMEOUT_MS = 150_000;
const PER_CALL_MAX_ATTEMPTS = 2;
const RETRY_BACKOFF_MS = [1500, 4500]; // index 0 = before attempt 2, etc.
const ANTHROPIC_URL = "https://api.anthropic.com/v1/messages";
const ANTHROPIC_MODEL = "claude-sonnet-4-6";

export type CallId = "a" | "b" | "c";

export interface PriorCallOutput {
  json: any; // the parsed sections payload returned by that call
}

export interface ThreeCallArgs {
  jobId: string;
  anthropicApiKey: string;
  masterSystemPrompt: string;          // existing SYSTEM_PROMPT
  chartScopedRulesShared: string;      // shared rules (Lilith/Juno gates, SR-year, etc.) WITHOUT chart data
  natalChartBlock: string;             // text block containing only natal chart data
  srChartBlock: string;                // text block containing only SR chart data ("(no SR provided)" if absent)
  effectiveCurrentDate: string;
  userQuestion: string;
  /**
   * Pre-rendered "VERIFIED CROSS-CHART ACTIVATIONS — GROUND TRUTH" block plus
   * the strict rules that follow it. Built deterministically by the caller via
   * computeCrossChartActivations + renderActivationsBlock + buildActivationRulesBlock.
   * Empty string disables the feature (legacy/fallback).
   */
  callCActivationsBlock?: string;
  /**
   * Per-chart deterministic retrograde summary for Call C — replaces the old
   * hardcoded leak ("SR Saturn Rx in Pisces …"). Format:
   *   "Natal retrograde: Jupiter, Chiron\nNatal direct: Sun, Moon, …\n
   *    SR retrograde: Saturn, Uranus, …\nSR direct: Sun, Moon, …"
   * Empty string falls back to no retrograde reminder.
   */
  callCRetrogradeSummary?: string;
  // Persisted output from prior partial run, keyed by call id. If present,
  // that call is skipped and its output is reused.
  priorOutputs?: Partial<Record<CallId, PriorCallOutput>>;
  // Updates ask_jobs row. Caller injects so this module stays storage-agnostic.
  updateJob: (patch: Record<string, any>) => Promise<void>;
}

