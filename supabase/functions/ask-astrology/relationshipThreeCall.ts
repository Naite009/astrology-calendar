/**
 * Compute the "RIGHT NOW TRANSIT WINDOWS" block from Call B's Relationship
 * Timing Windows output. Picks at most 2 transits whose exact peak (parsed
 * from date_range) falls within ±14 days of the current date. Returns a
 * "0 found" block if none qualify so the directive's empty-state branch fires.
 */
const buildRightNowBlock = (callBJson: any, effectiveCurrentDate: string): string => {
  const now = new Date(effectiveCurrentDate);
  const nowMs = isNaN(now.getTime()) ? Date.now() : now.getTime();
  const WINDOW_MS = 14 * 24 * 60 * 60 * 1000;
  const emptyBlock = (reason: string) => `=========================================================
RIGHT NOW TRANSIT WINDOWS — GROUND TRUTH (0 found)
=========================================================
${reason} Write the "Right Now" summary item per the empty-state instruction in the directive.`;

  const sections = Array.isArray(callBJson?.sections) ? callBJson.sections : [];
  const timing = sections.find((s: any) =>
    s?.type === "timing_section"
    && typeof s?.title === "string"
    && /timing\s+windows?/i.test(s.title)
  );
  const transits = Array.isArray(timing?.transits) ? timing.transits : [];
  if (transits.length === 0) {
    return emptyBlock("No timing windows were generated in Call B, so no \"right now\" transit can be cited.");
  }

  const parseRangeMidpoint = (raw: string): number | null => {
    if (!raw || typeof raw !== "string") return null;
    const cleaned = raw.replace(/[–—]/g, "-").trim();
    const tryParse = (s: string): number | null => {
      const d = new Date(s);
      if (!isNaN(d.getTime())) return d.getTime();
      const mY = s.match(/^([A-Za-z]+)\s+(\d{4})$/);
      if (mY) {
        const d2 = new Date(`${mY[1]} 15, ${mY[2]}`);
        if (!isNaN(d2.getTime())) return d2.getTime();
      }
      return null;
    };
    // Split on " to " or " - " (latter only when followed by capital/digit)
    const parts = cleaned.split(/\s+to\s+|\s+-\s+(?=[A-Z0-9])/i);
    if (parts.length >= 2) {
      const yearMatch = parts[parts.length - 1].match(/\b(\d{4})\b/);
      const head = /\d{4}/.test(parts[0])
        ? parts[0]
        : (yearMatch ? `${parts[0]}, ${yearMatch[1]}` : parts[0]);
      const startMs = tryParse(head);
      const endMs = tryParse(parts[parts.length - 1]);
      if (startMs != null && endMs != null) return Math.round((startMs + endMs) / 2);
      if (startMs != null) return startMs;
      if (endMs != null) return endMs;
    }
    return tryParse(cleaned);
  };

  const scored = transits
    .map((t: any) => {
      const peakMs = parseRangeMidpoint(t?.date_range || "");
      if (peakMs == null) return null;
      const distance = Math.abs(peakMs - nowMs);
      return { t, peakMs, distance };
    })
    .filter((x: any) => x && x.distance <= WINDOW_MS)
    .sort((a: any, b: any) => a.distance - b.distance)
    .slice(0, 2);

  if (scored.length === 0) {
    return emptyBlock(`No transit listed in Relationship Timing Windows has its exact peak within ±14 days of the current date (${effectiveCurrentDate}).`);
  }

  const lines = scored.map((s: any, i: number) => {
    const t = s.t;
    const planet = String(t.planet || "").trim();
    const aspect = String(t.aspect || "").trim();
    const natalPoint = String(t.natal_point || "").trim();
    const dateRange = String(t.date_range || "").trim();
    const interp = String(t.interpretation || "").trim().slice(0, 400);
    const days = Math.round(s.distance / (24 * 60 * 60 * 1000));
    return `  ${i + 1}. ${planet} ${aspect} ${natalPoint} — date_range: "${dateRange}" — peak is ~${days} day(s) from current date — Call B's interpretation: "${interp}"`;
  }).join("\n");

  return `=========================================================
RIGHT NOW TRANSIT WINDOWS — GROUND TRUTH (${scored.length} found)
=========================================================
The following transit(s) from Call B's Relationship Timing Windows have their exact peak within ±14 days of the current date (${effectiveCurrentDate}). The "Right Now" item in the Relationship Strategy Summary MUST be written from this list and ONLY from this list.

${lines}

Rules for using this block:
- Lead with the lived behavior the person would feel this week / this month, then name the transit by name (e.g., "Saturn squaring your natal Venus right now").
- Do NOT invent additional transits. Do NOT pull from the verified cross-chart activations list (those are year-long, not "right now").
- 2 to 4 sentences total for the "Right Now" value, regardless of how many transits are in the block.`;
};


export const runThreeCallRelationship = async (args: ThreeCallArgs): Promise<ThreeCallResult> => {
  const overallStart = Date.now();
  const {
    jobId, anthropicApiKey, masterSystemPrompt, chartScopedRulesShared,
    natalChartBlock, srChartBlock, effectiveCurrentDate, userQuestion,
    callCActivationsBlock, callCRetrogradeSummary,
    priorOutputs, updateJob,
  } = args;

  // Build A and B system stacks now. Call C's directive + user message are
  // built AFTER A+B settle so we can derive the "Right Now" block from Call B's
  // Relationship Timing Windows output.
  const sysBlocksA = buildSharedSystemBlocks(masterSystemPrompt, chartScopedRulesShared, DIRECTIVE_A, effectiveCurrentDate);
  const sysBlocksB = buildSharedSystemBlocks(masterSystemPrompt, chartScopedRulesShared, DIRECTIVE_B, effectiveCurrentDate);

  const userMsgA = buildCallAUserMessage(natalChartBlock, userQuestion);
  const userMsgB = buildCallBUserMessage(srChartBlock, userQuestion);

  // Helper that runs a single call, persists its output on success, and
  // patches call_status. Reuses prior output if present.
  const runAndPersist = async (
    callId: CallId,
    sysBlocks: Array<Record<string, any>>,
    userMessage: string,
  ): Promise<SingleCallResult & { from_cache: boolean }> => {
    const prior = priorOutputs?.[callId];
    if (prior?.json) {
      console.info(`[3call] Reusing prior output for call ${callId} (job ${jobId})`);
      return { json: prior.json, ms: 0, attempts: 0, from_cache: true };
    }
    const result = await runOneCallWithRetry(callId, sysBlocks, userMessage, anthropicApiKey);
    // Persist immediately so a future retry can resume from here.
    const colName = `call_${callId}_output` as const;
    const statusPatch: Record<string, any> = {};
    statusPatch[colName] = result.json;
    // Read-modify-write of call_status is racy, but A/B run in parallel and
    // each sets a different key; jsonb_set semantics from supabase-js client
    // would require a custom RPC. Cheaper: send a complete call_status snapshot
    // along with each update. This module owns call_status while the job is
    // running, so collisions are limited to the A/B parallel pair — we resolve
    // via a final reconciliation patch after Promise.all resolves.
    await updateJob(statusPatch);
    return { ...result, from_cache: false };
  };

  // ── Calls A + B in parallel ───────────────────────────────────────────
  const aPromise = runAndPersist("a", sysBlocksA, userMsgA);
  const bPromise = runAndPersist("b", sysBlocksB, userMsgB);

  const settled = await Promise.allSettled([aPromise, bPromise]);
  const aRes = settled[0];
  const bRes = settled[1];

  const aOk = aRes.status === "fulfilled";
  const bOk = bRes.status === "fulfilled";

  // Reconcile call_status now that A and B are both settled.
  const reconciledStatus: Record<string, "done" | "failed"> = {};
  if (priorOutputs?.a) reconciledStatus.a = "done";
  if (priorOutputs?.b) reconciledStatus.b = "done";
  if (priorOutputs?.c) reconciledStatus.c = "done";
  reconciledStatus.a = aOk ? "done" : "failed";
  reconciledStatus.b = bOk ? "done" : "failed";
  await updateJob({ call_status: reconciledStatus });

  if (!aOk || !bOk) {
    const errs: string[] = [];
    if (!aOk) errs.push(`A: ${(aRes as PromiseRejectedResult).reason?.message || "unknown"}`);
    if (!bOk) errs.push(`B: ${(bRes as PromiseRejectedResult).reason?.message || "unknown"}`);
    throw new Error(`3-call generation failed in parallel phase — ${errs.join(" | ")}`);
  }

  const aValue = (aRes as PromiseFulfilledResult<SingleCallResult & { from_cache: boolean }>).value;
  const bValue = (bRes as PromiseFulfilledResult<SingleCallResult & { from_cache: boolean }>).value;

  // ── Build Call C inputs (depend on Call B's output for "Right Now") ───
  const pinnedNatalConstraints = buildPinnedNatalConstraints(callCActivationsBlock || "");
  const rightNowBlock = buildRightNowBlock(bValue.json, effectiveCurrentDate);
  const hasRightNowBlock = /GROUND TRUTH \((?!0 found)/.test(rightNowBlock);
  const directiveC = buildDirectiveC(pinnedNatalConstraints, hasRightNowBlock);
  const sysBlocksC = buildSharedSystemBlocks(masterSystemPrompt, chartScopedRulesShared, directiveC, effectiveCurrentDate);
  const userMsgC = buildCallCUserMessage(
    natalChartBlock,
    srChartBlock,
    userQuestion,
    callCActivationsBlock || "",
    callCRetrogradeSummary || "",
    rightNowBlock,
    effectiveCurrentDate,
  );
  console.info(`[3call] Right Now block built: hasRightNow=${hasRightNowBlock}, blockLen=${rightNowBlock.length}`);

  // ── Call C sequentially after A+B succeed ─────────────────────────────
  // DIAGNOSTIC: confirm the verified-activations ground-truth block actually
  // reaches Call C's user message. If it doesn't, the model has nothing to
  // anchor its overlay prose to and will fall back to inventing aspects.
  {
    const marker = "VERIFIED CROSS-CHART ACTIVATIONS";
    const idx = userMsgC.indexOf(marker);
    const activationsArgLen = (callCActivationsBlock || "").length;
    const head = userMsgC.slice(0, 300).replace(/\s+/g, " ");
    const blockExcerpt = idx >= 0
      ? userMsgC.slice(idx, idx + 400).replace(/\s+/g, " ")
      : "(block not found in userMsgC)";
    // Find the directive block inside the system stack and excerpt it so we
    // can confirm at runtime that the new ABSOLUTE CONSTRAINT directive
    // actually shipped to the model.
    const sysJoined = sysBlocksC
      .map((b) => (typeof b?.text === "string" ? b.text : ""))
      .join("\n---\n");
    const dirMarker = "ABSOLUTE CONSTRAINT FOR THIS CALL";
    const dirIdx = sysJoined.indexOf(dirMarker);
    const directiveExcerpt = dirIdx >= 0
      ? sysJoined.slice(dirIdx, dirIdx + 500).replace(/\s+/g, " ")
      : "(ABSOLUTE CONSTRAINT directive NOT FOUND in system blocks)";
    console.info(
      `[ask-astrology][callC-diag] userMsg length=${userMsgC.length}, activationsBlock arg length=${activationsArgLen}, marker index=${idx}, directive index=${dirIdx}\n  userMsgC head(0..300): "${head}"\n  activations block excerpt: "${blockExcerpt}"\n  directive excerpt: "${directiveExcerpt}"`,
    );
  }
  let cValue: SingleCallResult & { from_cache: boolean };
  try {
    cValue = await runAndPersist("c", sysBlocksC, userMsgC);
    reconciledStatus.c = "done";
    await updateJob({ call_status: reconciledStatus });
  } catch (cErr: any) {
    reconciledStatus.c = "failed";
    await updateJob({ call_status: reconciledStatus });
    throw new Error(`3-call generation failed at overlay (Call C) — ${cErr?.message || String(cErr)}`);
  }

  // ── Merge sections in canonical order ─────────────────────────────────
  // Pluck each (call, title) entry from MERGE_ORDER. Sections that don't
  // exist in the call's output (e.g. SR placements when no SR chart) are
  // silently skipped — the existing pipeline already tolerates missing
  // optional sections.
  const callOutputs: Record<CallId, any> = {
    a: aValue.json,
    b: bValue.json,
    c: cValue.json,
  };

  const findSectionByTitle = (sections: any[], title: string): any | null => {
    if (!Array.isArray(sections)) return null;
    const t = title.toLowerCase().trim();
    for (const s of sections) {
      const st = typeof s?.title === "string" ? s.title.toLowerCase().trim() : "";
      if (st === t) return s;
    }
    // Loose match: contains (handles minor title drift like "The Relationship Pattern")
    for (const s of sections) {
      const st = typeof s?.title === "string" ? s.title.toLowerCase().trim() : "";
      if (st.includes(t) || t.includes(st)) return s;
    }
    return null;
  };

  const mergedSections: any[] = [];
  for (const entry of MERGE_ORDER) {
    if (entry.call === "code") continue; // injected by caller (polarity/elements)
    const out = callOutputs[entry.call as CallId];
    const section = findSectionByTitle(out?.sections, entry.title);
    if (section) {
      // Stamp provenance for debugging.
      section._source_call = entry.call;
      mergedSections.push(section);
    }
  }

  // Top-level metadata: take from any call; prefer A (natal-anchored).
  const merged: any = {
    ...(aValue.json && typeof aValue.json === "object" ? aValue.json : {}),
    sections: mergedSections,
    question_type: "relationship",
    _three_call: {
      enabled: true,
      a: { ms: aValue.ms, attempts: aValue.attempts, from_cache: aValue.from_cache },
      b: { ms: bValue.ms, attempts: bValue.attempts, from_cache: bValue.from_cache },
      c: { ms: cValue.ms, attempts: cValue.attempts, from_cache: cValue.from_cache },
      total_ms: Date.now() - overallStart,
    },
  };

  return {
    mergedJsonString: JSON.stringify(merged),
    diagnostics: {
      a: { ok: true, from_cache: aValue.from_cache, attempts: aValue.attempts, ms: aValue.ms },
      b: { ok: true, from_cache: bValue.from_cache, attempts: bValue.attempts, ms: bValue.ms },
      c: { ok: true, from_cache: cValue.from_cache, attempts: cValue.attempts, ms: cValue.ms },
      total_ms: Date.now() - overallStart,
    },
  };
};

// ─── Deterministic polarity / element / modality computation ───────────────
// Spec: Yang planets always: Sun, Mars, Jupiter, Saturn, Uranus
//       Yin planets always: Moon, Venus, Neptune, Pluto
//       Mercury: neutral, count toward Yang
// So Yang = Sun+Mercury+Mars+Jupiter+Saturn+Uranus = 6 planets always
//    Yin  = Moon+Venus+Neptune+Pluto = 4 planets always
// Total = 10. Sum is structurally guaranteed.

const PLANET_POLARITY: Record<string, "Yang" | "Yin"> = {
  Sun: "Yang", Mercury: "Yang", Mars: "Yang", Jupiter: "Yang", Saturn: "Yang", Uranus: "Yang",
  Moon: "Yin", Venus: "Yin", Neptune: "Yin", Pluto: "Yin",
};

const SIGN_ELEMENT: Record<string, "Fire" | "Earth" | "Air" | "Water"> = {
  Aries: "Fire", Leo: "Fire", Sagittarius: "Fire",
  Taurus: "Earth", Virgo: "Earth", Capricorn: "Earth",
  Gemini: "Air", Libra: "Air", Aquarius: "Air",
  Cancer: "Water", Scorpio: "Water", Pisces: "Water",
};

const SIGN_MODALITY: Record<string, "Cardinal" | "Fixed" | "Mutable"> = {
  Aries: "Cardinal", Cancer: "Cardinal", Libra: "Cardinal", Capricorn: "Cardinal",
  Taurus: "Fixed", Leo: "Fixed", Scorpio: "Fixed", Aquarius: "Fixed",
  Gemini: "Mutable", Virgo: "Mutable", Sagittarius: "Mutable", Pisces: "Mutable",
};

const COUNTED_PLANETS = Object.keys(PLANET_POLARITY); // exactly 10

export interface DeterministicTallies {
  elements: Array<{ name: string; tag: string; count: number; planets: string[] }>;
  modalities: Array<{ name: string; tag: string; count: number; planets: string[] }>;
  polarity: Array<{ name: string; tag: string; count: number; planets: string[] }>;
}

/**
 * Parse natal chart text (lines like "Sun: 12°34' Capricorn (House 1)") and
 * return code-computed element / modality / polarity counts. The polarity
 * tally is fixed by planet identity (never derived from sign), so the total
 * is structurally guaranteed to be 10 regardless of what the AI does.
 */
/**
 * Extract a single planetary-positions block ("NATAL" or "SR") from the
 * full ask-astrology chart context string. Returns the empty string if the
 * requested header is not present. Used so the deterministic balance
 * computation never accidentally folds NATAL planet signs into an SR
 * section (or vice versa) when the full context contains both blocks.
 */
export const extractPositionsBlock = (
  chartContext: string,
  source: "NATAL" | "SR",
): string => {
  if (!chartContext) return "";
  // Match "NATAL Planetary Positions:" or "SR Planetary Positions:" exactly.
  const headerRe =
    source === "SR"
      ? /\bSR Planetary Positions:\s*\n/
      : /\bNATAL Planetary Positions:\s*\n/;
  const headerMatch = chartContext.match(headerRe);
  if (!headerMatch) return "";
  const startIdx = headerMatch.index! + headerMatch[0].length;
  const tail = chartContext.slice(startIdx);
  // Stop at the next blank line or another all-caps section header.
  const endMatch = tail.match(
    /\n\s*\n|\n[A-Z][A-Z ]{6,}:|\nHouse Cusps|\nPlanets In Each|\nRuler Chains|\nVERIFIED|\nSR House Cusps|\nSR-TO-NATAL|\nNATAL Planetary Positions|\nSR Planetary Positions/,
  );
  return endMatch ? tail.slice(0, endMatch.index!) : tail;
};

export const computeDeterministicTallies = (chartBlock: string, opts?: { source?: "NATAL" | "SR" }): DeterministicTallies => {
  // If the caller passes the full chart context (containing both NATAL and
  // SR blocks) and tells us which source they want, slice the right block
  // first. Otherwise use whatever string they passed (back-compat: relationship
  // 3-call already passes a single isolated block).
  const block = opts?.source ? extractPositionsBlock(chartBlock, opts.source) || chartBlock : chartBlock;
  const planetSign: Record<string, string> = {};
  // SR-prefixed lines look like "- SR Sun: 12°34' Capricorn". We strip the
  // optional "SR " prefix so the planet key is just "Sun" (matches NATAL keys
  // and COUNTED_PLANETS).
  const re = /^[\s\-\*•]*(?:SR\s+)?([A-Za-z][A-Za-z\s]*?):\s*\d+°\d+'\s+(Aries|Taurus|Gemini|Cancer|Leo|Virgo|Libra|Scorpio|Sagittarius|Capricorn|Aquarius|Pisces)\b/gm;
  let m: RegExpExecArray | null;
  while ((m = re.exec(block)) !== null) {
    const planet = m[1].trim();
    const sign = m[2];
    if (!planetSign[planet]) planetSign[planet] = sign;
  }

  const elements = { Fire: 0, Earth: 0, Air: 0, Water: 0 };
  const modalities = { Cardinal: 0, Fixed: 0, Mutable: 0 };
  const polarity = { Yang: 0, Yin: 0 };

  const elementPlanets: Record<string, string[]> = { Fire: [], Earth: [], Air: [], Water: [] };
  const modalityPlanets: Record<string, string[]> = { Cardinal: [], Fixed: [], Mutable: [] };
  const polarityPlanets: Record<string, string[]> = { Yang: [], Yin: [] };

  for (const planet of COUNTED_PLANETS) {
    const sign = planetSign[planet];
    if (sign && SIGN_ELEMENT[sign]) {
      elements[SIGN_ELEMENT[sign]]++;
      elementPlanets[SIGN_ELEMENT[sign]].push(planet);
    }
    if (sign && SIGN_MODALITY[sign]) {
      modalities[SIGN_MODALITY[sign]]++;
      modalityPlanets[SIGN_MODALITY[sign]].push(planet);
    }
    polarity[PLANET_POLARITY[planet]]++; // polarity by planet identity, sign-independent
    polarityPlanets[PLANET_POLARITY[planet]].push(planet);
  }

  // Emit BOTH `name` (consumed by hygiene/validators/UI) AND `tag`
  // (legacy field) so every downstream reader works on every reading
  // type. Without `name`, `correctModalityElementCounts`,
  // `enforceNonZeroCoverage`, the Replit gate, and the PDF renderer all
  // see the entries as nameless and report 0 counts even though the
  // integer is correct in the array. The "Yang (Active)" / "Yin
  // (Receptive)" suffix on polarity matches the prompt's stub format so
  // the loose match in correctModalityElementCounts (firstWord = "yang"
  // / "yin") and the explicit lookups still resolve.
  return {
    elements: (["Fire", "Earth", "Air", "Water"] as const).map((tag) => ({ name: tag, tag, count: elements[tag], planets: elementPlanets[tag] })),
    modalities: (["Cardinal", "Fixed", "Mutable"] as const).map((tag) => ({ name: tag, tag, count: modalities[tag], planets: modalityPlanets[tag] })),
    polarity: (["Yang", "Yin"] as const).map((tag) => {
      const name = tag === "Yang" ? "Yang (Active)" : "Yin (Receptive)";
      return { name, tag, count: polarity[tag], planets: polarityPlanets[tag] };
    }),
  };
};

/**
 * Build a `modality_element` section object from deterministic tallies.
 * The `balance_interpretation` field is intentionally left empty — the
 * existing `enforceNonZeroCoverage` hygiene pass will fill it with a
 * coverage clause that names every non-zero element/modality/polarity.
 */
export const buildModalityElementSection = (tallies: DeterministicTallies): any => {
  const pickDominant = (arr: Array<{ name: string; count: number }>): string => {
    const max = arr.reduce((a, b) => (b.count > a.count ? b : a), arr[0]);
    return max?.name ?? "";
  };
  return {
    type: "modality_element",
    title: "Natal Elemental & Modal Balance",
    elements: tallies.elements,
    modalities: tallies.modalities,
    polarity: tallies.polarity,
    dominant_element: pickDominant(tallies.elements as any),
    dominant_modality: pickDominant(tallies.modalities as any),
    dominant_polarity: pickDominant(tallies.polarity as any),
    balance_interpretation: "",
    body: "",
    _deterministic: true,
  };
};

/**
 * Insert (or replace) the modality_element section in the merged sections
 * array at the spot indicated by MERGE_ORDER. Idempotent.
 *
 * PRESERVATION RULE: when replacing an existing modality_element section,
 * any non-empty `body` or `balance_interpretation` the AI authored is
 * preserved on the new section. We only overwrite the deterministic count
 * arrays (elements/modalities/polarity) and dominant_* fields; prose is
 * AI-authored and must survive the inject so downstream body backfills
 * have something to copy from.
 */
export const injectDeterministicModalityElement = (
  parsedContent: any,
  chartContext: string,
): { injected: boolean; tallies: DeterministicTallies } => {
  if (!parsedContent || !Array.isArray(parsedContent.sections)) {
    return { injected: false, tallies: { elements: [], modalities: [], polarity: [] } };
  }

  const isNonEmptyString = (v: unknown): v is string =>
    typeof v === "string" && v.trim().length > 0;

  // Detect whether the existing modality_element section is SR-flavored
  // by its title. If so, deterministic counts MUST be computed from the
  // SR Planetary Positions block, not the natal block — otherwise the
  // section's prose ends up describing natal element/modality balance
  // under an SR title and the external gate flags BALANCE_CLAIM_MISMATCH.
  const existingIdx = parsedContent.sections.findIndex((s: any) => s?.type === "modality_element");
  const existing = existingIdx >= 0 ? parsedContent.sections[existingIdx] : null;
  const existingTitle = isNonEmptyString(existing?.title) ? existing.title : "";
  const isSrSection = /\b(solar\s*return|sr)\b/i.test(existingTitle);

  // Pick the right block. If the caller passed the full chartContext,
  // we extract the matching block; if they passed a pre-isolated natal
  // block (relationship 3-call path), the extractor falls back to the
  // input string and behavior is unchanged.
  const tallies = computeDeterministicTallies(chartContext, { source: isSrSection ? "SR" : "NATAL" });
  const newSection = buildModalityElementSection(tallies);
  if (isSrSection) {
    newSection.title = existingTitle || "Solar Return Elemental & Modal Balance";
  }

  // Replace any existing modality_element section, otherwise insert at the
  // canonical slot (just before the summary_box at the end).
  if (existingIdx >= 0) {
    const prev = existing || {};
    // Preserve AI-authored prose so the body backfill has source material.
    if (isNonEmptyString(prev.body)) newSection.body = prev.body;
    if (isNonEmptyString(prev.balance_interpretation)) {
      newSection.balance_interpretation = prev.balance_interpretation;
    }
    if (isNonEmptyString(prev.title)) newSection.title = prev.title;
    parsedContent.sections[existingIdx] = newSection;
    return { injected: true, tallies };
  }
  // Insert before the first summary_box; if none, append.
  const summaryIdx = parsedContent.sections.findIndex((s: any) => s?.type === "summary_box");
  const insertAt = summaryIdx >= 0 ? summaryIdx : parsedContent.sections.length;
  parsedContent.sections.splice(insertAt, 0, newSection);
  return { injected: true, tallies };
};

/**
 * After merge, overwrite any AI-authored polarity counts in any
 * modality_element section with the deterministic values. Defensive:
 * even if a future call sneaks one in, polarity counts are guaranteed
 * to sum to 10 in the final JSON.
 */
export const overwriteAllPolarityCounts = (
  parsedContent: any,
  chartContext: string,
): number => {
  if (!parsedContent || !Array.isArray(parsedContent.sections)) return 0;
  // Pre-compute tallies for both possible sources so each section gets
  // numbers from the correct chart (natal vs SR). For relationship 3-call
  // path the input is already an isolated natal block — extractPositionsBlock
  // returns "" and computeDeterministicTallies falls back to the full input,
  // so behavior on that path is unchanged.
  const natalTallies = computeDeterministicTallies(chartContext, { source: "NATAL" });
  const srTallies = computeDeterministicTallies(chartContext, { source: "SR" });
  let overwritten = 0;
  for (const section of parsedContent.sections) {
    if (section?.type !== "modality_element") continue;
    const title = typeof section.title === "string" ? section.title : "";
    const isSr = /\b(solar\s*return|sr)\b/i.test(title);
    const t = isSr ? srTallies : natalTallies;
    // Only overwrite when the chosen source actually parsed planets, so a
    // missing SR block can't blank an SR section's counts to all zeros.
    const hasData = t.polarity.some((p) => p.count > 0);
    if (!hasData) continue;
    section.polarity = t.polarity;
    section.elements = t.elements;
    section.modalities = t.modalities;
    overwritten++;
  }
  return overwritten;
};

// ─── Resume support: read prior outputs from the ask_jobs row ──────────────

export interface AskJobRow {
  call_a_output?: any;
  call_b_output?: any;
  call_c_output?: any;
  call_status?: Record<string, "done" | "failed"> | null;
}

/**
 * Build a priorOutputs map from a job row, only including calls whose
 * status was "done" AND whose output column actually contains data.
 * If call_status is empty (fresh job), returns {}.
 */
export const buildPriorOutputs = (row: AskJobRow): Partial<Record<CallId, PriorCallOutput>> => {
  const status = row.call_status || {};
  const out: Partial<Record<CallId, PriorCallOutput>> = {};
  if (status.a === "done" && row.call_a_output) out.a = { json: row.call_a_output };
  if (status.b === "done" && row.call_b_output) out.b = { json: row.call_b_output };
  if (status.c === "done" && row.call_c_output) out.c = { json: row.call_c_output };
  return out;
};
